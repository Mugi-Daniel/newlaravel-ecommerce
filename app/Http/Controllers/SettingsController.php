        $setting->About =$request->input('about');
        $setting->Email =$request->input('email');
        $setting->Phone_number =$request->input('phone');
        $setting->Mpesa_paybill =$request->input('paybill');
        $setting->Save();

        return back()->with('success', 'Settings Saved');
    }

